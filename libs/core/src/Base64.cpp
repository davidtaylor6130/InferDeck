std::vector<uint8_t> Base64Decode(const std::string& input) {
    if (input.empty()) return {};

    std::string cleaned;
    cleaned.reserve(input.size());
    for (auto c : input) {
        if (c != '=' && kDecodeTable[static_cast<unsigned char>(c)] == 0xFF) continue;
        cleaned += c;
    }

    if (cleaned.empty()) return {};

    size_t padding = 0;
    if (!cleaned.empty() && cleaned.back() == '=') { ++padding; cleaned.pop_back(); }
    if (!cleaned.empty() && cleaned.back() == '=') { ++padding; cleaned.pop_back(); }

    size_t output_len = (cleaned.size() / 4) * 3;
    if (padding == 1) output_len += 2;
    else if (padding == 2) output_len += 1;

    std::vector<uint8_t> result;
    result.reserve(output_len);

    for (size_t i = 0; i + 3 < cleaned.size(); i += 4) {
        uint32_t a = kDecodeTable[static_cast<unsigned char>(cleaned[i])];
        uint32_t b = kDecodeTable[static_cast<unsigned char>(cleaned[i + 1])];
        uint32_t c = kDecodeTable[static_cast<unsigned char>(cleaned[i + 2])];
        uint32_t d = kDecodeTable[static_cast<unsigned char>(cleaned[i + 3])];
        uint32_t triple = (a << 18) | (b << 12) | (c << 6) | d;
        result.push_back(static_cast<uint8_t>((triple >> 16) & 0xFF));
        result.push_back(static_cast<uint8_t>((triple >> 8) & 0xFF));
        result.push_back(static_cast<uint8_t>(triple & 0xFF));
    }

    size_t remaining = cleaned.size() % 4;
    if (remaining >= 2) {
        size_t pos = (cleaned.size() / 4) * 4;
        uint32_t a = kDecodeTable[static_cast<unsigned char>(cleaned[pos])];
        uint32_t b = kDecodeTable[static_cast<unsigned char>(cleaned[pos + 1])];
        uint32_t triple = (a << 18) | (b << 12);
        if (remaining >= 3) {
            uint32_t c = kDecodeTable[static_cast<unsigned char>(cleaned[pos + 2])];
            triple |= (c << 6);
        }
        result.push_back(static_cast<uint8_t>((triple >> 16) & 0xFF));
        if (remaining >= 3) {
            result.push_back(static_cast<uint8_t>((triple >> 8) & 0xFF));
        }
    }

    return result;
}

